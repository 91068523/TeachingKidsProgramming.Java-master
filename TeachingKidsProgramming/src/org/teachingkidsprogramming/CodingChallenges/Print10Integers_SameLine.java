package org.teachingkidsprogramming.CodingChallenges;

public class Print10Integers_SameLine
{
  public static void main(String[] args)
  {
    for (int i = 0; i < 10; i++)
    {
      System.out.print(i);
    }
    System.out.println();
    for (int i = 1; i < 11; i++)
    {
      System.out.print(i);
    }
  }
}
